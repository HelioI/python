def soma(* valores):
    s = 0
    for num in valores:
        s += num
    print(f'Somando os valores {valores} temos {s} ')


# Programa principal
soma(5, 2)
soma(8, 1, 3, 0, 8)
soma(9, 1, 4, 5)



